//* Assignment Operator
let r = 5;

//* Addition assignment
r = r + 1;
r += 1;

//* Subtraction assignment
r = r - 1;
r -= 1; 

//* Multiplication assignment
r = r * 1;
r *= 1;

//* Division assignment
r = r / 1;
r /= 1;

//* Remainder/Modulus
r = r % 1;
r %= 1;

//* Exponential
r = r ** 1;
r **= 1;

