//* Comparison Operators

//* Equal
'3' == 3;

//* Strictly Equal
3 === 3;

//* Not equal
console.log(3 != 4);

//* Strictly not equal
console.log('3' !== 3);

//* Greater than
3 > 2;

//* Less than
2 < 3;

//* Greater than or equal to
3 >= 2;

//* lesser than or equal to
2 <= 3;

//* And
2 && 3;

//* Or
2 || 3;

let x = 40;
console.log(x < 50 && x == 20);



